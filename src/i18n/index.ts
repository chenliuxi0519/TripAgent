const DICTS: Record<Language, Dict> = { zh, en }

export function translate(lang: Language, key: string, vars?: Record<string, string | number>): string {
  let s = DICTS[lang]?.[key] ?? DICTS.zh[key] ?? key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, "g"), String(v))
    }
  }
  return s
}

/** Non-hook accessor (for services). */
export function t(key: string, vars?: Record<string, string | number>): string {
  return translate(useUiStore.getState().language, key, vars)
}

/** Hook: returns a `t` bound to the current (reactive) language. */
export function useT() {
  const lang = useUiStore((s) => s.language)
  return {
    lang,
    t: (key: string, vars?: Record<string, string | number>) => translate(lang, key, vars),
  }
}
